#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>
#include <stdbool.h>
#include <assert.h>


typedef uint8_t   u8; // byte
typedef uint16_t u16; // halfword
typedef uint32_t u32; // word 
typedef uint64_t u64; // double word

typedef int8_t  s8;  // signed byte
typedef int16_t s16; // signed halfword
typedef int32_t s32; // signed word
typedef int64_t s64; // signed double word